import React from 'react';
import {
  SafeAreaView,
  ScrollView,
  StatusBar,
  StyleSheet,
  Text,
  useColorScheme,
  View,
} from 'react-native';

import {check, request, PERMISSIONS, RESULTS} from 'react-native-permissions';

import {
  Colors,
  DebugInstructions,
  Header,
  LearnMoreLinks,
  ReloadInstructions,
} from 'react-native/Libraries/NewAppScreen';

const Section: React.FC<{
  title: string;
}> = ({children, title}) => {
  const isDarkMode = useColorScheme() === 'dark';
  const permissionCheck = check(PERMISSIONS.IOS.LOCATION_WHEN_IN_USE);
  permissionCheck.then(result => {
    switch (result) {
      case RESULTS.UNAVAILABLE:
        throw new Error(
          'Location permission not available on this device / in this context',
        );
      case RESULTS.DENIED: {
        // persmission has not yet been requested / is denied but is requestable
        let req = request(PERMISSIONS.IOS.LOCATION_WHEN_IN_USE);
        req.then(result => {
          switch (result) {
            case RESULTS.GRANTED:
              console.log('yay');
              break;
            default:
              throw new Error('the user rejected location permissions');
          }
        });
        break;
      }
      case RESULTS.GRANTED:
        console.log('yay');
        break;
      case RESULTS.BLOCKED:
        throw new Error(
          'Location permission is denied and not requestable anymore',
        );
    }
  });
  return (
    <View style={styles.sectionContainer}>
      <Text
        style={[
          styles.sectionTitle,
          {
            color: isDarkMode ? Colors.white : Colors.black,
          },
        ]}>
        {title}
      </Text>
      <Text
        style={[
          styles.sectionDescription,
          {
            color: isDarkMode ? Colors.light : Colors.dark,
          },
        ]}>
        {children}
      </Text>
    </View>
  );
};

